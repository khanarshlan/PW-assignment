function generateGreeting(name){
    return `Welcome to the java script program, ${name}`
}

let personOne = generateGreeting("aditya")
let personTwo = generateGreeting("Teji")
let personThree = generateGreeting("sagar")

console.log(personOne)   // Welcome to the java script program, aditya
console.log(personTwo)   // Welcome to the java script program, Teji
console.log(personThree) // Welcome to the java script program, sagar